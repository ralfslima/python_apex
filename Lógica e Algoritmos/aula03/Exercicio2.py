# Obter número inicial
print("Informe o número inicial")
inicio = int(input())

# Obter número final
print("Informe o número final")
termino = int(input())

# Laço For
for indice in range(inicio, termino+1):
    print(indice)

# Divisão
print("-----------------")

# Laço While
while inicio <= termino:
    print(inicio)
    inicio+=1

